    private String removePunct(String str) {
        // Create a temp placeholder for incoming Strings from file
        String temp = "";
        // Put the strings into a character array to remove punctuation
        for (Character c : str.toCharArray()) {
            // Check if the characters are letters
            if (Character.isLetterOrDigit(c)) // Add that character to temp
            {
                temp += c;
            }
        }
        // return temp
        return temp.toLowerCase();
    }


