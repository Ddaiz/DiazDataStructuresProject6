    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
        try {
            if (words.get(txtFind.getText().toLowerCase()) != null) {
                lblFound.setText("Word: " + txtFind.getText() + " has been found.");
            } // otherwise
            else {
                // display this message
                lblFound.setText("Word: " + txtFind.getText() + " can not be found.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "An error occure while trying to find your word.");
        }
    }//GEN-LAST:event_btnFindActionPerformed

    private void btnDisplayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDisplayActionPerformed
        Scanner fileScan = null;

        try {
            fileScan = new Scanner(new File(fileName));

            while (fileScan.hasNext()) {
                String fileWord = fileScan.next();
                String alpha = removePunct(fileWord);
                words.put(alpha, count);
                myList.add(alpha);
                count++;
            }

            Set<String> unique = new HashSet<String>(myList);
            for (String key : unique) {
                txtWordListCount.append(Collections.frequency(myList, key) + "\n");
            }

            Set<String> keys = words.keySet();

            Iterator<String> myIterator = keys.iterator();

            while (myIterator.hasNext()) {
                hashStrings = myIterator.next();
                txtKeyList.append(words.get(hashStrings) + "\n");
                txtWordList.append(hashStrings + "\n");
            }
            txtKeyList.setCaretPosition(0);
            txtWordList.setCaretPosition(0);
            txtWordListCount.setCaretPosition(0);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "File not found or an error occured.");
        }
    }//GEN-LAST:event_btnDisplayActionPerformed

    private void btnGetFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGetFileActionPerformed
        JFileChooser source = new JFileChooser();
        int returnedFile = source.showOpenDialog(this);
        if (returnedFile == JFileChooser.APPROVE_OPTION) {
            file = source.getSelectedFile();
            fileName = file.getAbsolutePath();
            txtFileName.setText(fileName);
        }
    }//GEN-LAST:event_btnGetFileActionPerformed

